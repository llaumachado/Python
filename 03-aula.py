# variáveis em python
# não há  necessidade de declará-lás no inicio do programa
# basta criar no meio do algoritmo com a atribuição

teste = "Laura"
print(teste) #imprime a string "Laura"
print (type(teste)) #imprime o tipo da variavel,string
teste = 10
print (teste) #imprime o numero 10
print(type(teste))# imprime o tipo da variavel,que int
teste = 1.74
print(teste) #imprime o numero 1.74
print(type(teste))# imprime o tipo da variavel,que é float
teste = True
print(teste) #imprime o valor true
print(type(teste)) # imprime o tipo da variavel, que é bool



 
